function generateRandomInsult() {
  const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const noun = nouns[Math.floor(Math.random() * nouns.length)];
  return `${adjective} ${noun}`;
}

function stringToBinary(string) {
    const charCodes = string.split("").map(char => char.charCodeAt(0));

    const binaryStrings = charCodes.map(charCode => {
        let binaryString = charCode.toString(2);

        while (binaryString.length < 8) {
            binaryString = "0" + binaryString;
        }

        return binaryString;
    });

    const binaryString = binaryStrings.join("");

    return binaryString;
}

function lmao() {
    return stringToBinary(generateRandomInsult());
}

